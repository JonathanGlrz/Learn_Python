### Operadores Artimeticos ###
print(3 + 4)  # Suma
print(3 - 4)  #Resta
print(3 * 4)  # Multiplicación
print(10 / 3)  # División
print(10 % 3)  #Calcula lo que resta de una division 10/3, sobra 1
print(10 // 3)  # Division aproximada a un numero entero
print(2 ** 3)  # Calcular un exponente

print("Hola" + "Python") # Solo el + concatena mas tipos de variables
print("Hola" + str(5)) # Para mezclar cadena str con numeros tenemos que modificar el valor INT
print("Hola" * 5) # Podemos multiplicar los valores añadidos en el print

### Operadores Comparativos ###

print(3 > 4) # Mayor que 4
print(3 < 4) # Menor que 4
print(3 >= 4) # Menor o igual que 4
print(3 <= 4) # Mayor o igual 4
print(3 == 4) # Es igual que 4
print(3 != 4) # Es diferente a 4
print("baaa" >= "aab") # Ordenacion alfabetica (No cuenta caracteres)
print(len("aaa") <+ len("aaba")) # En este caso si que cuneta los caracteres de cada valor

### Operadores Logicos ###

print(3 > 4 and "Hola" > "Python")
print(3 > 4 or "Hola" > "Python")
print(3 < 4 and "Hola" < "Python")
print(3 < 4 or "Hola" < "Python")
print(3 < 4 or ("Hola" > "Python" and 4==4))
print(not(3 > 4))




